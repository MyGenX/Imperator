"""Shared rendering helpers and the renderer protocol.

Holds the Jinja environment, rule-block rendering, ownership-marker handling,
the file-write helper, and the `Renderer` base class + `RenderContext` that the
per-target renderers (claude/cursor/codex/gemini) and the compiler use.
"""

