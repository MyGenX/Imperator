_role_instruction_doc = role_instruction_doc


# ── Renderer protocol ────────────────────────────────────────────────────────

@dataclass
class RenderContext:
    """Everything a renderer needs to emit one project's native layout."""
    out_dir: Path
    domains: list[str]               # selected domain names (selection order)
    roles: list[str]                 # selected role names
    role_defs: list[Role]            # loaded Role objects
    global_groups: list[RuleGroup]   # agent-filtered global rule groups
    domain_groups: list[RuleGroup]   # agent-filtered domain rule groups
    style: str


class Renderer:
    """Base class for per-agent native-layout renderers."""

    agent: str = ""

    def compile(self, ctx: RenderContext) -> list[Path]:  # pragma: no cover - abstract
        raise NotImplementedError
